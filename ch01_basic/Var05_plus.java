public class Var05_plus {
    public static void main(String[] args) {
        int a=3;
        int b=7;
        double c=1.0;
        int d=6;

        a+=1;
        b+=a;
        c-=b;
        d/=2;

        System.out.println("a= "+a);
        System.out.println("b= "+b);
        System.out.println("c= "+c);
        System.out.println("d= "+d);
        
    }    
}
